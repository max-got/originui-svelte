<script lang="ts">
	import Label from '$lib/components/ui/label.svelte';
	import * as Select from '$lib/components/ui/select/index.js';

	import Sun from 'lucide-svelte/icons/sun';
	import Moon from 'lucide-svelte/icons/moon';
	import MonitorCog from 'lucide-svelte/icons/monitor-cog';

	const items = [
		{ value: 's1', label: 'Light', icon: Sun },
		{ value: 's2', label: 'Dark', icon: Moon },
		{ value: 's3', label: 'System', icon: MonitorCog }
	];

	let value = $state('s1');

	const selected = $derived(items.find((i) => i.value === value));
</script>

{#snippet theme(item: (typeof items)[number])}
	<svelte:component this={item.icon} size={16} aria-hidden="true" />
	<span class="truncate">{item.label}</span>
{/snippet}

<div class="space-y-2">
	<Label for="select-34">Options with icon</Label>
	<Select.Root type="single" bind:value>
		<Select.Trigger
			id="select-34"
			class="[&>span]:flex [&>span]:items-center [&>span]:gap-2 [&>span_svg]:shrink-0 [&>span_svg]:text-muted-foreground/80"
		>
			{#if selected}
				{@render theme(selected)}
			{:else}
				Select a theme
			{/if}
		</Select.Trigger>
		<Select.Content
			class="[&_*[data-select-item]>span>svg]:shrink-0 [&_*[data-select-item]>span>svg]:text-muted-foreground/80 [&_*[data-select-item]>span]:flex [&_*[data-select-item]>span]:gap-2"
		>
			{#each items as item (item.value)}
				<Select.Item value={item.value}>
					{@render theme(item)}
				</Select.Item>
			{/each}
		</Select.Content>
	</Select.Root>
</div>
